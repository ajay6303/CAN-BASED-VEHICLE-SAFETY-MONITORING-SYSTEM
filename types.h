#ifndef _TYPES_H_
#define _TYPES_H_

// --- Fixed-Width Unsigned Integers ---
typedef unsigned char u8;         // 1-byte unsigned data (0 to 255)
typedef unsigned short int u16;   // 2-bit unsigned data (0 to 65,535)
typedef unsigned int u32;         // 4-byte unsigned data (0 to 4,294,967,295)

// --- Fixed-Width Signed and Floating Types ---
typedef signed int s32;           // 4-byte signed integer data (-2,147,483,648 to 2,147,483,647)
typedef float f32;                // 4-byte single-precision floating point

#endif
